def generate_basque_recipe_translations():
    """
    Generate complete Basque translations for all recipes using AI.
    """
    print("=== GENERATING BASQUE RECIPE TRANSLATIONS WITH AI ===")

    conn = get_db_connection()
    cursor = conn.cursor()

    # Get all recipes
    cursor.execute(
        "SELECT id, title, description, ingredients, instructions, category FROM recipes"
    )
    recipes = cursor.fetchall()

    print(f"Found {len(recipes)} recipes to translate to Basque")

    # Delete existing Basque translations
    cursor.execute("DELETE FROM recipe_translations WHERE language = 'eu'")
    print("Deleted existing Basque translations")

    translated_count = 0

    for recipe in recipes:
        recipe_id, title, description, ingredients, instructions, category = recipe

        print(f"\nTranslating recipe {recipe_id}: '{title}'")

        # Translate each field with AI
        basque_title = translate_to_basque_ai(title, "title")
        basque_description = translate_to_basque_ai(description, "description")
        basque_ingredients = translate_to_basque_ai(ingredients, "ingredients")
        basque_instructions = translate_to_basque_ai(instructions, "instructions")
        basque_category = translate_to_basque_ai(category, "category")

        print(f"  Title: '{title}' -> '{basque_title}'")
        print(f"  Category: '{category}' -> '{basque_category}'")

        # Save translation to database
        try:
            cursor.execute(
                """
                INSERT INTO recipe_translations (recipe_id, language, title, description, ingredients, instructions, category)
                VALUES (?, ?, ?, ?, ?, ?, ?)
                """,
                (
                    recipe_id,
                    "eu",
                    basque_title,
                    basque_description,
                    basque_ingredients,
                    basque_instructions,
                    basque_category,
                ),
            )
            translated_count += 1
        except Exception as e:
            print(f"  ERROR saving Basque translation: {e}")
            continue

        if translated_count % 10 == 0:
            conn.commit()
            print(f"Progress: {translated_count}/{len(recipes)} recipes translated")

    conn.commit()
    conn.close()

