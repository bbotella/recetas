    conn = get_db_connection()
    cursor = conn.cursor()
    
    print("🏮 Updating Chinese translations for remaining 40 recipes...")
    for recipe_id, chinese_desc in CHINESE_TRANSLATIONS_REMAINING.items():
        cursor.execute(
            "UPDATE recipe_translations SET description = ? WHERE recipe_id = ? AND language = 'zh'",
            (chinese_desc, recipe_id)
        )
        print(f"Updated Chinese for recipe {recipe_id}")
    
    conn.commit()
    conn.close()
    print("\n✅ All remaining Chinese translations updated successfully!")

