def generate_complete_translations():
    """Generate complete AI translations for all recipes."""
    print("Initializing database...")
    init_database()
    
    print("Getting all recipes...")
    recipes = get_all_recipes()
    
    print(f"Found {len(recipes)} recipes to translate with complete AI translations.")
    
    translations = get_complete_recipe_translations()
    
    translated_count = 0
    updated_count = 0
    
    for recipe in recipes:
        print(f"Processing recipe: {recipe['title']}")
        
        # Check if translation already exists
        existing_translation = get_recipe_translation(recipe['id'], 'en')
        
        if existing_translation:
            print(f"  - Translation exists, updating with complete AI translation...")
            updated_count += 1
        else:
            print(f"  - Creating new complete AI translation...")
            translated_count += 1
        
        # Get AI translation
        if recipe['title'] in translations:
            translation = translations[recipe['title']]
        else:
            # For any recipe not in our complete list, use basic category translation
            translation = {
                'title': recipe['title'],
                'description': recipe['description'],
                'ingredients': recipe['ingredients'],
                'instructions': recipe['instructions'],
                'category': CATEGORY_TRANSLATIONS.get(recipe['category'], recipe['category'])
            }
            print(f"  - Using basic translation for {recipe['title']}")
        
        # Save translation
        save_recipe_translation(
            recipe['id'],
            'en',
            translation['title'],
            translation['description'],
            translation['ingredients'],
            translation['instructions'],
            translation['category']
        )
        
        print(f"  - Complete AI translation saved successfully")
    
    print(f"\nComplete AI Translation finished!")
    print(f"  - New translations: {translated_count}")
    print(f"  - Updated translations: {updated_count}")
    print(f"  - Total recipes: {len(recipes)}")

if __name__ == "__main__":
    generate_complete_translations()